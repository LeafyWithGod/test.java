package 集合存储学生进阶;

public class student_ {
    String name;
    int age;

    public student_(){}
    public student_(String name,int age)
    {
        this.name=name;
        this.age=age;
    }

    public void setname(String name)
    {this.name=name;}
    public String getname()
    {return name;}

    public void setage(int age)
    {this.age=age;}
    public int getage()
    {return age;}
}
